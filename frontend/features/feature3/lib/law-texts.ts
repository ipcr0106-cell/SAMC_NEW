export function getLawText(lawSource: string) {
  // 정확 매칭 먼저
  if (LAW_TEXTS[lawSource]) return LAW_TEXTS[lawSource];

  // 부분 매칭 (law_source가 길어서 세미콜론으로 분리된 경우)
  for (const [key, value] of Object.entries(LAW_TEXTS)) {
    if (lawSource.includes(key) || key.includes(lawSource)) return value;
  }

  // 조항 번호로 매칭
  const articleMatch = lawSource.match(/제27조제1항제(\d+(?:의\d+)?)호/);
  if (articleMatch) {
    for (const [key, value] of Object.entries(LAW_TEXTS)) {
      if (key.includes(articleMatch[0])) return value;
    }
  }

  return null;
}
