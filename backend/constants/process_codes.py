def get_code_label(code: str) -> str:
    """공정 코드에 해당하는 한글 라벨을 반환. 없으면 코드 자체 반환."""
    name = PROCESS_CODE_MAP.get(code)
    return f"{code} - {name}" if name else code


def get_prompt_table() -> str:
    """시스템 프롬프트에 포함할 공정 코드 참조 테이블 문자열."""
    lines = []
    for code, name in PROCESS_CODE_MAP.items():
        lines.append(f"{code}={name}")
    return ", ".join(lines)
