# ─────────────────────────────────────────
# 데이터 구조화 → DataFrame
# ─────────────────────────────────────────


def build_rows(sources: list[tuple]) -> list[dict]:
    rows = []
    for item in sources:
        food_type, condition, doc_name, doc_description, law_source = item
        rows.append(
            {
                "food_type": food_type,
                "condition": condition,
                "doc_name": doc_name,
                "doc_description": doc_description,
                "is_mandatory": True,
                "law_source": law_source,
            }
        )
    return rows


# ─────────────────────────────────────────
# CSV 저장 (검수용)
# ─────────────────────────────────────────


def save_csv(rows: list[dict], path: Path):
    df = pd.DataFrame(rows)
    df.to_csv(path, index=False, encoding="utf-8-sig")
    print(f"  → CSV 저장: {path.name}  ({len(df)}행)")


# ─────────────────────────────────────────
# Supabase 업로드
# ─────────────────────────────────────────


def upload_to_supabase(rows: list[dict], batch_size: int = 50):
    total = len(rows)
    uploaded = 0

    for i in range(0, total, batch_size):
        batch = rows[i : i + batch_size]
        supabase.table("f2_required_documents").insert(batch).execute()
        uploaded += len(batch)
        print(f"  → {uploaded}/{total} 업로드 완료")


